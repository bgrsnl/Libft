#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	x;
	int	res;

	i = 0;
	x = 1;
	res = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			x = -x;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}	
	return (res * x);
}
